package cli

import (
	"fmt"
	"io"

	"github.com/spf13/cobra"
)

var (
	Version string
)

func NewCmdVersion(out io.Writer) *cobra.Command {
	versionCmd := &cobra.Command{
		Use:   "version",
		Short: "Print version information",
		Run: func(cmd *cobra.Command, args []string) {
			fmt.Fprintf(out, Version)
		},
	}

	return versionCmd
}
